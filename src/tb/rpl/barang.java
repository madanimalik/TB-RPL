    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String cekkode = txtkode.getText();
        String ceknama = txtnama.getText();
        String cekharga = txtharga.getText();
        String cekjumlah = txtjumlah.getText();
        
        if(cekkode.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Kode Barang belum di isi!");
        }else if(ceknama.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Barang belum di isi!");
        }else if(cekjumlah.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Jumlah belum di isi!");
        }else if(cekharga.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harga belum di isi!");
        }else{
            try{
                st.executeUpdate("INSERT INTO barang VALUES("
                        +"'"+txtkode.getText()+"',"
                        +"'"+txtnama.getText()+"',"
                        +"'"+txtharga.getText()+"',"
                        +"'"+txtjumlah.getText()+"');");                           
                        JOptionPane.showMessageDialog(this, "Data barang berhasil disimpan!");
                        kosongkan();
                        isitable();
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Salah Pengisan Data");
            }
        } 
    }//GEN-LAST:event_btnTambahActionPerformed

