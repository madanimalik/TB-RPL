    void isitable(){
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn("ID Barang");
        t.addColumn("Nama Barang");
        t.addColumn("Harga Barang");
        t.addColumn("Jumlah Barang");
        tbl.setModel(t);        
        try {
            rs = st.executeQuery("select * from barang");
            while(rs.next())
            { t .addRow(new Object []{
                rs.getString("id_barang"),
                rs.getString("nama_barang"),
                rs.getString("harga_barang"),
                rs.getString("jumlah_barang"),
            });                       
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }  
    }
    
   
    
