    private void btntambahjumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahjumlahActionPerformed
        String nama1 = txtnama.getText();
        String harga1 = txtharga.getText();
        int tampung = Integer.parseInt(txtjumlah.getText());
        int tambah = Integer.parseInt(txttmbhbarng.getText());
        int hasil = tampung + tambah;                
        
        try { 
            st.executeUpdate("update barang set "  
                    + "nama_barang='"+nama1+"',"
                    + "harga_barang='"+harga1+"',"
                    + "jumlah_barang='"+hasil+"'"
                    + " where "
                    + "id_barang='"+txtkode.getText()+"';"
                    ); 
            kosongkan();             
            isitable();
            JOptionPane.showMessageDialog(rootPane , txtnama+"berhasil Ditambah"); 
            } catch (SQLException | HeadlessException e) { 
            JOptionPane.showMessageDialog(rootPane, e); 
            } 
    }//GEN-LAST:event_btntambahjumlahActionPerformed

